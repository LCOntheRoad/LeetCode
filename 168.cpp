/************************************************************************/
/* 26进制，需要注意的是没有从0开始   和171题思路一样                    */
/************************************************************************/

class Solution {
public:
	string convertToTitle(int n) {
		string res = "";
		char temp;
		while (n)
		{
			temp = (n-1)%26+'A';
			res = temp+ res;
			n = (n-1)/26;
		}

		return res;

	}
};